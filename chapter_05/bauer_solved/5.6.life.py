age = 65

if age < 2:
    print('Jesteś niemowlęciem.')
elif 4 > age >= 2:
    print('Jesteś dzieckiem który uczy się chodzić.')
elif 4 <= age < 13:
    print('Jesteś dzieckiem.')
elif 20 > age >= 13:
    print('Jesteś nastolatkiem.')
elif 65 > age >= 20:
    print('Jesteś dorosłym.')
elif 65 <= age:
    print('Jesteś seniorem.')